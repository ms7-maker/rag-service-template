from typing import List, Dict, Any


SYSTEM_MESSAGE = (
    "Ты - полезный AI ассистент, который отвечает на вопросы "
    "на основе предоставленного контекста."
)


def build_rag_prompt(query: str, context_docs: List[Dict[str, Any]]) -> str:
    """Собрать пользовательский промпт из контекста и вопроса."""
    context_parts = []
    for i, doc in enumerate(context_docs, 1):
        context_parts.append(f"Документ {i}:\n{doc['text']}\n")

    context = "\n".join(context_parts)

    return f"""Ты - полезный AI ассистент. Ответь на вопрос пользователя на основе предоставленного контекста.

Контекст:
{context}

Вопрос: {query}

Инструкции:
- Отвечай только на основе предоставленного контекста
- Если в контексте нет информации для ответа, скажи об этом
- Будь точным и кратким
- Отвечай на русском языке

Ответ:"""
